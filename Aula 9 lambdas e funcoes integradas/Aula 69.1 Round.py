"""
round() -> retorna um número arrendonda para n digitos de precisão após a casa decimal. Se a precisão não for informada
retorna o inteiro mais próximo da entrada

"""

print(round(10.2))  # default é zero para quantidade de casas decimais (logo vai arrendondar para inteiro)
print(round(1.234445,2))  # arrendondar na segunda casa decimal