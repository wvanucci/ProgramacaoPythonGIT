# dias da semana informando o numero 1-7

n = int(input('Qual o número da semana: '))
if n == 1:
    print('Segunda-feira')
elif n == 2:
    print('Terça - feira')
elif n == 3:
    print('Quarta-feira')
elif n == 4:
    print('Quinta-feira')
elif n == 5:
    print('Sexta-feira')
elif n == 6:
    print('Sábado')
elif n == 7:
    print('Domingo')
else:
    print('não sei que dia é')

