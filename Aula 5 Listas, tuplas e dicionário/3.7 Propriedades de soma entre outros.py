#Soma, valor max, valor min, tamanho (somente se os valores int ou float)
receita = {'jan':10,'fev':200,'mar':400}
print(sum(receita.values()))  #soma dos valores
print(max(receita.values()))  #max dos valores
print(min(receita.values()))  #minimo dos valores
print(len(receita))    #tamanho do dict (quantidade de elemento)


