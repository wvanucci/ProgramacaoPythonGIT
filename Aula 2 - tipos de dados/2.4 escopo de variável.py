"""Escopo de variável

Dois casos de escopo:
1 - Variável Globais: São reconhecidas, ou seja, seu escopo compreende todo o programa
2 _Variáveis Locais: São reconhecidas apenas no bloco onde foram DECLARADAS, ou seja seu escopo está limitada
ao bloco onde foi declarada

para declara variável em python:

nome_da_váriavel = valor_da_variável

Python é uma linguagem de tipagem dinâmica: Isso significa que ao declararmos uma variável, nós não colocamos o tipo de
dela. Este tipo é inferido ao atribuírmos o valor á mesma.

Exemplo em Java:
int numero =42
No python não precisamos atribuir int

"""